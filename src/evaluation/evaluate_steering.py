def keyword_score(text: str, trait: str) -> float:
    if trait not in BFI_ITEMS:
        return 0.0
    entry = BFI_ITEMS[trait]
    pos_kw = entry.get("pos_keywords", [])
    neg_kw = entry.get("neg_keywords", [])
    lower = text.lower()
    pos_hits = sum(1 for k in pos_kw if k in lower)
    neg_hits = sum(1 for k in neg_kw if k in lower)
    total = pos_hits + neg_hits
    if total == 0:
        return 0.0
    return (pos_hits - neg_hits) / total


def compute_perplexity(model, tokenizer, text: str, device: str) -> float:
    inputs = tokenizer(text, return_tensors="pt").to(device)
    input_ids = inputs["input_ids"]
    if input_ids.shape[1] < 2:
        return float("inf")
    with torch.no_grad():
        outputs = model(**inputs, labels=input_ids)
    loss = outputs.loss
    return float(torch.exp(loss).item())
